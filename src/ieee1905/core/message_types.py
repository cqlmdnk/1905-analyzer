"""IEEE 1905.1 CMDU message types.

Values per IEEE 1905.1-2013 §17.1 (Table 17-1) plus the amendments. The
EasyMesh additions (0x8000+) land in Phase 2.
"""

from __future__ import annotations

from enum import IntEnum


class MessageType(IntEnum):
    """CMDU message type identifiers."""

    TOPOLOGY_DISCOVERY = 0x0000
    TOPOLOGY_NOTIFICATION = 0x0001
    TOPOLOGY_QUERY = 0x0002
    TOPOLOGY_RESPONSE = 0x0003
    VENDOR_SPECIFIC = 0x0004
    LINK_METRIC_QUERY = 0x0005
    LINK_METRIC_RESPONSE = 0x0006
    AP_AUTOCONFIGURATION_SEARCH = 0x0007
    AP_AUTOCONFIGURATION_RESPONSE = 0x0008
    AP_AUTOCONFIGURATION_WSC = 0x0009
    AP_AUTOCONFIGURATION_RENEW = 0x000A
    PUSH_BUTTON_EVENT_NOTIFICATION = 0x000B
    PUSH_BUTTON_JOIN_NOTIFICATION = 0x000C
    HIGHER_LAYER_QUERY = 0x000D
    HIGHER_LAYER_RESPONSE = 0x000E
    INTERFACE_POWER_CHANGE_REQUEST = 0x000F
    INTERFACE_POWER_CHANGE_RESPONSE = 0x0010
    GENERIC_PHY_QUERY = 0x0011
    GENERIC_PHY_RESPONSE = 0x0012

    # ---- Wi-Fi EasyMesh R1 (Multi-AP Specification v1.0) -------------------
    # Spec: Multi-AP Specification v1.0 §17.1, message types 0x8000-0x801B.

    EM_ACK = 0x8000
    EM_AP_CAPABILITY_QUERY = 0x8001
    EM_AP_CAPABILITY_REPORT = 0x8002
    EM_MULTI_AP_POLICY_CONFIG_REQUEST = 0x8003
    EM_CHANNEL_PREFERENCE_QUERY = 0x8004
    EM_CHANNEL_PREFERENCE_REPORT = 0x8005
    EM_CHANNEL_SELECTION_REQUEST = 0x8006
    EM_CHANNEL_SELECTION_RESPONSE = 0x8007
    EM_OPERATING_CHANNEL_REPORT = 0x8008
    EM_CLIENT_CAPABILITY_QUERY = 0x8009
    EM_CLIENT_CAPABILITY_REPORT = 0x800A
    EM_AP_METRICS_QUERY = 0x800B
    EM_AP_METRICS_RESPONSE = 0x800C
    EM_ASSOCIATED_STA_LINK_METRICS_QUERY = 0x800D
    EM_ASSOCIATED_STA_LINK_METRICS_RESPONSE = 0x800E
    EM_UNASSOCIATED_STA_LINK_METRICS_QUERY = 0x800F
    EM_UNASSOCIATED_STA_LINK_METRICS_RESPONSE = 0x8010
    EM_BEACON_METRICS_QUERY = 0x8011
    EM_BEACON_METRICS_RESPONSE = 0x8012
    EM_COMBINED_INFRASTRUCTURE_METRICS = 0x8013
    EM_CLIENT_STEERING_REQUEST = 0x8014
    EM_CLIENT_STEERING_BTM_REPORT = 0x8015
    EM_CLIENT_ASSOCIATION_CONTROL_REQUEST = 0x8016
    EM_STEERING_COMPLETED = 0x8017
    EM_HIGHER_LAYER_DATA = 0x8018
    EM_BACKHAUL_STEERING_REQUEST = 0x8019
    EM_BACKHAUL_STEERING_RESPONSE = 0x801A

    # ---- Wi-Fi EasyMesh R2 message types -----------------------------------
    # Values verified against strict R3-compliant controllers ieee_1905_1::eMessageType. R2 and R3
    # extensions are packed densely in the 0x801B-0x8031 range; the previous
    # ordering had off-by-one shifts that put BackhaulSTA Capability Query
    # at 0x8026 (actually Tunnelled) and Report at 0x8027 (actually Query).
    EM_CHANNEL_SCAN_REQUEST = 0x801B
    EM_CHANNEL_SCAN_REPORT = 0x801C
    EM_CAC_REQUEST = 0x8020
    EM_CAC_TERMINATION = 0x8021
    EM_CLIENT_DISASSOCIATION_STATS = 0x8022
    EM_SERVICE_PRIORITIZATION_REQUEST = 0x8023
    EM_ERROR_RESPONSE = 0x8024
    EM_ASSOCIATION_STATUS_NOTIFICATION = 0x8025
    EM_TUNNELED = 0x8026
    EM_BACKHAUL_STA_CAPABILITY_QUERY = 0x8027
    EM_BACKHAUL_STA_CAPABILITY_REPORT = 0x8028
    EM_FAILED_CONNECTION = 0x8033

    # ---- Wi-Fi EasyMesh R3 message types -----------------------------------
    EM_DPP_CCE_INDICATION = 0x801D
    EM_1905_REKEY_REQUEST = 0x801E
    EM_1905_DECRYPTION_FAILURE = 0x801F
    EM_CAC_STATUS_REPORT = 0x8032
    EM_PROXIED_ENCAP_DPP = 0x8034
    EM_DIRECT_ENCAP_DPP = 0x8035
    EM_RECONFIG_TRIGGER = 0x8036
    EM_BSS_CONFIGURATION_REQUEST = 0x8037
    EM_BSS_CONFIGURATION_RESPONSE = 0x8038
    EM_BSS_CONFIGURATION_RESULT = 0x8039
    EM_CHIRP_NOTIFICATION = 0x803A
    EM_1905_ENCAP_EAPOL = 0x803B
    EM_DPP_BOOTSTRAPPING_URI_NOTIFICATION = 0x803C
    EM_AGENT_LIST = 0x8043

    # ---- Wi-Fi EasyMesh R4 message types -----------------------------------
    EM_AVAILABLE_SPECTRUM_INQUIRY = 0x8044
    EM_AVAILABLE_SPECTRUM_INQUIRY_RESPONSE = 0x8045
    EM_QOS_MANAGEMENT_NOTIFICATION = 0x8046

    @classmethod
    def describe(cls, value: int) -> str:
        """Human-readable label, falling back to a hex code for unknowns."""
        try:
            return cls(value).name
        except ValueError:
            return f"Unknown(0x{value:04x})"
